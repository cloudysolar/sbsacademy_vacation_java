package com.cloudysolar.lessons.day03;

// 비교(관계) 연산자 : <, >, <=, >=, ==, !=

public class Lesson03_05 {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 20;

		System.out.println(num1 + " < " + num2 + " = " + (num1 < num2));
		System.out.println(num1 + " > " + num2 + " = " + (num1 > num2));
		System.out.println(num1 + " <= " + num2 + " = " + (num1 <= num2));
		System.out.println(num1 + " >= " + num2 + " = " + (num1 >= num2));
		System.out.println(num1 + " == " + num2 + " = " + (num1 == num2));
		System.out.println(num1 + " != " + num2 + " = " + (num1 != num2));
	}
}
